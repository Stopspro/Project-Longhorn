// scrypt allows 45 characters in one line, 20 in a new buffer, and 25 in the main bufffer if there is a 2nd buffer open.
pub fn scryptconfig() {
  let starter = String::from("~ ");
  let version = 0.1;
  let maxchar_norm = 45;
  let newwin_maxchar = 20;
  let conwin_maxchar = 15;
  if newwin = 1 {
    let maxchar_norm = 25;
  }
}

